<h1>Tangkal Berita Hoax, Pemerintah Luncurkan Situs Covid-19 </h1>
<p>
	<ul>
		Maraknya kabar berita bohong atau hoax di tengah mewabahnya virus corona (covid-19) meresahkan semua pihak. Hal ini membuat pemerintah melalui Gugus Tugas Percepatan Penanganan covid-19 hari ini (18/3/2020) meluncurkan portal www.covid19.go.id sebagai sumber informasi resmi penanggulangan virus corona, yang kini menjadi pandemi global covid-19. Situs ini diharapkan bisa menjadi rujukan resmi untuk informasi satu pintu mengenai virus corona dan bagaimana mengendalikannya.
	</ul>

	<ul>
		''Kami menyadari, masyarakat butuh akses pada informasi akurat, cepat dan terpercaya. Untuk itulah mengapa situs www.covid19.go.id ini dibuat agar bisa menjadi sumber informasi resmi satu pintu,'' ujar Ketua Gugus Tugas Percepatan Penanganan covid-19 Doni Monardo.
	</ul>

	<ul>
		Situs www.covid19.go.id dikembangkan oleh Tim Komunikasi Risiko dan Pelibatan Masyarakat untuk Penanggulangan covid-19. Tim terdiri dari berbagai elemen, yaitu pemerintah, Badan PBB (UNICEF, WHO, dll), mitra pembangunan internasional, organisasi masyarakat sipil, dan dunia usaha.
		Doni menjelaskan, situs www.covid19.go.id memuat pesan tentang tiga langkah penting yang bisa dilakukan masyarakat di tengah wabah virus corona. Tiga langkah itu yaitu Cara Mengurangi Risiko Penularan, Cari Informasi yang Benar, dan Apa yang Perlu Dilakukan bila Sakit.
	</ul>

	<ul>
		Ada pula Hoax-Buster yang bisa digunakan sebagai acuan untuk menentukan apakah sebuah informasi tentang Covid-19 merupakan berita benar atau hoaks. Materi Edukasi berisi mengenai berbagai materi edukasi publik. 
		''Kami berkomitmen memberikan informasi yang akurat dan bertanggung jawab kepada masyarakat. Pengetahuan dan perilaku yang benar adalah elemen penting untuk melawan penularan penyakit ini,'' ujar Doni Monardo. Doni juga menegaskan, upaya menghadapi covid-19 hanya bisa dilakukan jika semua bekerjasama dan disiplin mematuhi protokol kesehatan dari pemerintah. ''Di antaranya menjaga jarak aman dengan bekerja, beribadah, belajar dari rumah serta selalu melakukan praktik kebersihan dasar, khususnya cuci tangan menggunakan sabun,'' pungkas Kepala BNPB ini. 	
	</ul>
