<h1>Pencegahan Virus Corona</h1>
<p>Sampai saat ini, belum ada vaksin untuk mencegah infeksi virus Corona atau COVID-19. Oleh sebab itu, cara pencegahan yang terbaik adalah dengan menghindari faktor-faktor yang bisa menyebabkan Anda terinfeksi virus ini, yaitu:
</p>
<ul>
	<li>Hindari bepergian ke tempat-tempat umum yang ramai pengunjung.</li>
	<li>Gunakan masker saat beraktivitas di tempat umum atau keramaian.</li>
	<li>Rutin mencuci tangan dengan air dan sabun atau hand sanitizer yang mengandung alkohol minimal 60% setelah beraktivitas di luar rumah atau di tempat umum.</li>
	<li>Jangan menyentuh mata, mulut, dan hidung sebelum mencuci tangan</li>
	<li>Hindari kontak dengan hewan, terutama hewan liar. Bila terjadi kontak dengan hewan, cuci tangan setelahnya.</li>
	<li>Masak daging sampai benar-benar matang sebelum dikonsumsi.</li>
	<li>Tutup mulut dan hidung dengan tisu saat batuk atau bersin, kemudian buang tisu ke tempat sampah.</li>
	<li>Hindari berdekatan dengan orang yang sedang sakit demam, batuk, atau pilek.</li>
	<li>Jaga kebersihan benda yang sering disentuh dan kebersihan lingkungan</li>
</ul>

<p>Untuk orang yang diduga terkena COVID-19, ada beberapa langkah yang bisa dilakukan agar virus Corona tidak menular ke orang lain, yaitu:</p>
<ul>
	<li>Jangan keluar rumah, kecuali untuk mendapatkan pengobatan.</li>
	<li>Periksakan diri ke dokter hanya bila Anda mengalami gejala atau keluhan.</li>
	<li>Usahakan untuk tinggal terpisah dari orang lain untuk sementara waktu. Bila tidak memungkinkan, gunakan kamar tidur dan kamar mandi yang berbeda dengan yang digunakan orang lain.</li>
	<li>Larang dan cegah orang lain untuk mengunjungi atau menjenguk Anda sampai Anda benar-benar sembuh.</li>
	<li>Sebisa mungkin jangan melakukan pertemuan dengan orang yang sedang sedang sakit.</li>
	<li>Hindari berbagi penggunaan alat makan dan minum, alat mandi, serta perlengkapan tidur dengan orang lain.</li>
	<li>Pakai masker dan sarung tangan bila sedang berada di tempat umum atau sedang bersama orang lain.</li>
	<li>Gunakan tisu untuk menutup mulut dan hidung bila batuk atau bersin, lalu segera buang tisu ke tempat sampah.</li>
</ul>
