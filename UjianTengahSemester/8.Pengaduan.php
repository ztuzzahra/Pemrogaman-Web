<h1>Berikut Layanan Yang dapat di akses</h1>
<img src="coronaa.jpg" width="600" height="300" alt="Photo">
<p>Sejak wabah corona merebak di beberapa wilayah dan sejumlah pemerintah daerah mengeluarkan kebijakan bekerja dari rumah hingga menutup sekolah selama dua pekan, operator seluler mencatat adanya lonjakan trafik di area permukiman rata-rata sebesar 5-10 persen dari kondisi normal. Kementerian Kominfo menyatakan, jika terus terjadi lonjakan trafik, operator seluler akan melakukan penambahan kapasitas pada masing-masing Base Transceiver Station (BTS).
</p>
<p> Berikut Beberapa Layanan Informasi yang dapat di hubungi :</p>
<ul>
	<li>
	Situs Resmi <a href="https://www.covid19.go.id/">https://www.covid19.go.id/</a> merupakan situs resmi pemerintah yang menyediakan perkembangan informasi virus corona di Indonesia
	</li>
	<br>
	<li>
	SMS blast Kementerian Kominfo bekerja sama dengan Kementerian Kesehatan (Kemenkes) dan Badan Nasional Penangggulangan Bencana (BNPB) untuk terus menyebarluaskan informasi mengenai pandemi Covid-19 di Tanah Air. BNPB akan mengirimkan SMS blast melalui operator seluler.
	</li>
	<br>
	<li>
	Selain SMS blast, juga tersedia call center resmi penanganan Covid-19 ke nomor 119. Lebih lanjut, nomor pelayanan darurat 117 digunakan BNPB yang merupakan layanan bebas pulsa atau gratis. Dalam informasi yang dihimpun, operator telekomunikasi diminta untuk sesegera mungkin membuka koneksi ini.
	</li>
	<br>
	<li>
	Public Service Announcement (PSA) Masyarakat juga dapat mengakses informasi terkait Covid-19 melalui lembaga penyiaran publik dan swasta, baik radio, televisi dan informasi yang disediakan oleh Penyedia Layanan Akses Internet (ISP) dan penyedia jasa media online dalam bentuk banner online.
	</li>
	<br>
	<li>
	Chatbot WA Chatbot ini akan rilis pada Jumat, 20 Maret 2020. Fasilitas ini bekerja sama dengan WhatsApp dan Telkom Group. Nantinya, masyarakat dapat berinteraksi dengan akun chat ini, untuk memperoleh informasi secara interaktif di mana kontennya disediakan oleh BNPB dan Kemenkes.
	</li>
	<br>
	<li>
	WhatsApp blast Publik yang bersedia menerima pesan blast mengenai virus corona SARS-CoV-2 di Indonesia, akan mendapatkan informasi mengenai pandemi global ini. Perusahaan WhatsApp hanya memberikan akses ini di Indonesia dan Singapura. WhatsApp blast rencananya akan mulai aktif pada 20 Maret 2020 malam.	
	</li>
</ul>
